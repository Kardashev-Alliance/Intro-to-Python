# ========================================
# Module 3: Conditional Statements in Python
# Section 1.2: The 'if' Statement
# ========================================

# A simple if statement
temperature = 25

# Check if temperature is above 30
if temperature > 30:
    print("It's a hot day!")

# ========================================
# Syntax of 'if' Statement
# ========================================

# Using 'if' with strings
user_name = "Alice"

if user_name == "Alice":
    print("Hello, Alice!")

# ========================================
# Everyday Analogy
# ========================================

# If it's raining, carry an umbrella
is_raining = False

if is_raining:
    print("Carry an umbrella!")

# Note: The above won't print anything since is_raining is set to False.

# ========================================
# More 'if' Statements
# ========================================

age = 18

if age >= 18:
    print("You're eligible to vote!")

score = 85

if score >= 50:
    print("Congratulations! You passed the exam.")

