# Lists are ordered collections that can hold items of different data types.
# They are mutable, which means their content can be changed after creation.

# -----------------------------------------------
# 1. Definition and Basics
# -----------------------------------------------
# Creating a basic list of integers
my_list = [1, 2, 3, 4, 5]
print(f"Basic List: {my_list}")

# A list can contain different data types.
mixed_list = [1, "apple", 3.14, True]
print(f"Mixed Data Types List: {mixed_list}")


# -----------------------------------------------
# 2. Accessing Elements
# -----------------------------------------------
# Accessing the first element
first_element = my_list[0]
print(f"First Element: {first_element}")

# Accessing the last element
last_element = my_list[-1]
print(f"Last Element: {last_element}")


# -----------------------------------------------
# 3. Lists Are Mutable
# -----------------------------------------------
# Changing the second element to 10
my_list[1] = 10
print(f"List after changing second element: {my_list}")


